Why we use?
Object-oriented programming (OOP) is a way of thinking about and organizing code for maximum reusability.
OOP helps to keep the PHP code DRY "Don't Repeat Yourself", and makes the code easier to maintain, modify and debug

Advantage
1.We can build the programs from standard working modules that communicate with one another, rather than having to start
writing the code from scratch which leads to saving of development time and higher productivity.
2.00P systems can be easily upgraded from small to large systems.
3.It is very easy to partition the work in a project based on objects.

Disadvantage
1. The length of the programmes developed using OOP language is much larger than the procedural approach. Since the
programme becomes larger in size, it requires more time to be executed that leads to slower execution of the programme.
2.We can not apply OOP everywhere as it is not a universal language. It is applied only when it is required. It is not
suitable for all types of problems.
3. Programmers need to have brilliant designing skill and programming skill along with proper planning because using OOP
is little bit tricky.